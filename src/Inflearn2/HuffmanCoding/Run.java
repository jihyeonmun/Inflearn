package Inflearn2.HuffmanCoding;

class Run{
    public byte symbol;
    public byte runLen;
    public int freq;

    public byte getSymbol() {
        return symbol;
    }

    public void setSymbol(byte symbol) {
        this.symbol = symbol;
    }

    public byte getRunLen() {
        return runLen;
    }

    public void setRunLen(byte runLen) {
        this.runLen = runLen;
    }

    public int getFreq() {
        return freq;
    }

    public void setFreq(int freq) {
        this.freq = freq;
    }
}
